START TRANSACTION;

DROP TABLE IF EXISTS additional_mentor_informations, mentors;

CREATE TABLE mentors (
  id INTEGER UNSIGNED NOT NULL AUTO_INCREMENT,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  family_name_ja VARCHAR(255) NOT NULL DEFAULT '',
  given_name_ja VARCHAR(255) NOT NULL DEFAULT '',
  family_name_en VARCHAR(255) NOT NULL DEFAULT '',
  given_name_en VARCHAR(255) NOT NULL DEFAULT '',
  works_at VARCHAR(255) NOT NULL DEFAULT '',
  graduated_from VARCHAR(255) NOT NULL DEFAULT '',
  dance_club VARCHAR(255) NOT NULL DEFAULT '',
  image MEDIUMBLOB NOT NULL,
  PRIMARY KEY (id)
) CHARACTER SET utf8;

CREATE TABLE additional_mentor_informations (
  id INTEGER UNSIGNED NOT NULL AUTO_INCREMENT,
  created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  title VARCHAR(255) NOT NULL DEFAULT '',
  content TEXT NOT NULL,
  mentor_id INTEGER UNSIGNED NOT NULL,
  PRIMARY KEY (id),
  FOREIGN KEY (mentor_id) REFERENCES mentors (id) ON DELETE CASCADE
) CHARACTER SET utf8;

COMMIT;
